    private void jbtn_connectActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtn_connectActionPerformed

        try {
            this.msj = "Type Connection " + this.buttonGroupConectionType.getSelection().getActionCommand() + "\n";
            
            this.jbtn_connect.setEnabled(false);
            this.jrdb_tcp.setEnabled(false);
            this.jrdb_udp.setEnabled(false);
            this.jtf_port.setEnabled(false);
            
            this.jtp_consol.setEditable(true);
            appendToPane(jtp_consol, msj, Color.blue);
            this.jtp_consol.setEditable(false);
            
            if (jtf_port.getText().isEmpty() || jtf_port.getText().equals("")) {
                jtf_port.setText("13");
                this.repaint();
                controlador = new ControllerServerDayTime(this, Integer.parseInt("13"));
            } else {
                controlador = new ControllerServerDayTime(this, Integer.parseInt(jtf_port.getText()));
            }
            
            controlador.conectar(this.tipoConeccion);
        } catch (InterruptedException ex) {
            Logger.getLogger(JPServer.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jbtn_connectActionPerformed


