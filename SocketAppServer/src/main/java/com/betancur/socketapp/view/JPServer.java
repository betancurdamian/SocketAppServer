    @Override
    public void actionPerformed(ActionEvent e) {
        if (jrdb_tcp.isSelected()) {
            this.buttonGroupConectionType.isSelected(jrdb_tcp.getModel());
            this.tipoConeccion = 1;
        }

        if (jrdb_udp.isSelected()) {
            this.buttonGroupConectionType.isSelected(jrdb_udp.getModel());
            this.tipoConeccion = 2;
        }
    }

    public void appendToPane(JTextPane tp, String msg, Color c) {
        StyleContext sc = StyleContext.getDefaultStyleContext();
        AttributeSet aset = sc.addAttribute(SimpleAttributeSet.EMPTY, StyleConstants.Foreground, c);

        aset = sc.addAttribute(aset, StyleConstants.FontFamily, "Lucida Console");
        aset = sc.addAttribute(aset, StyleConstants.Alignment, StyleConstants.ALIGN_JUSTIFIED);

        int len = tp.getDocument().getLength();
        tp.setCaretPosition(len);
        tp.setCharacterAttributes(aset, false);
        tp.replaceSelection(msg);
    }

